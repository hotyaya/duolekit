package cn.rdm.biz;

/**
 * 为数据库maindevice表中ORGC
 * 
 * @author Hui
 *
 */
public class OrganizationCode {
	
	public String orgc = "0000000";
	
	public String getOrgc() {
		return j+d+cj+gq;
	}

	private String j = "0";
	private String d = "00";
	private String cj = "00";
	private String gq = "00";
	
	public String getJ() {
		return j;
	}
	public void setJ(String j) {
		this.j = j;
	}
	public String getD() {
		return d;
	}
	public void setD(String d) {
		this.d = d;
	}
	public String getCj() {
		return cj;
	}
	public void setCj(String cj) {
		this.cj = cj;
	}
	public String getGq() {
		return gq;
	}
	public void setGq(String gq) {
		this.gq = gq;
	}
}
